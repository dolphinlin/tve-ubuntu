          <p>
            　　本所旨在培養工程、管理、設計，以及人文科學等領域之教育及訓練高級專業人才，包括學校教師、研究人本所以培育技職教育碩士班為主軸，接續碩士在職專班為輔，互相輔助，並結合師資培育等教育職前教育人才培育，並延續博士(在職)班以培育技職教育與訓練高級研究人才。由高職教師、主管、技專校院專學講師、職學訓練中心訓練師以及產業管理高階主管為銜接主軸，再配合本所教育目標及落實本校e化、國際化、產學化之教育政策，本所教育目標如下：
          </p>

<br>

          <h4><strong>
          碩士班
          </strong></h4>
            　　教育目標旨在培養工程、管理、設計，以及人文科學等領域之教育及訓練高級專業人才，包括學校教師、研究人員、企業界講師及教育行政人才，教育重點：
<br>
          <li>
            輔導研究生加強有關技術及職業教育理論與實務之研習，培養研究生具備技職教育之教學能力、研究能力和行政能力。
          </li>
          <li>
            輔導研究生依據其個人背景與需要，加強有關工程、管理、設計，以及人文科學等專門領域科技之研習，期使研究生提升其相關領域之專門知識和技術。
          </li>
<br>

          <h4><strong>
            博士班
          </strong></h4>
            　　教育目標旨在培育技職教育與訓練領域的高級研究人才，招生以在職人員為主，包含在職的技專校院教師、高職教師、以及產業界從事與教育訓練工作有關之在職人員，著重應用研究導向課程，並可在本校其他碩、博士班課程選修若干課程加強其原有的專門領域知能，教育重點：
<br>
          <li>
            應兼具規劃與執行研究的能力，且亦能從所處情境背景、區域社會文化、當地產業科技與經濟發展等脈絡，探索、發展、綜合與評析國內外技職教育發展的問題與實務。
          </li>

          <li>
            需能將研究成果應用於實際工作情境，以期提升其專門領域工作品質與效率。
          </li>
